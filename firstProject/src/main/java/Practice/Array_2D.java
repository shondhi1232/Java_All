package Practice;

public class Array_2D {
    public static void main(String[] args) {

        int [][] BazaarPrice={{2,3},{8,1}};

        for (int i=0;i<BazaarPrice.length;i++){
            for (int j=0;j<BazaarPrice.length;j++){
                System.out.println(BazaarPrice[i][j]);
            }

        }


    }
}
