package Practice;

public class Array_2D {
    public static void main(String[] args) {

        int [][] BazarPrice={{2,3},{8,1}};

        for (int i=0;i<BazarPrice.length;i++){
            for (int j=0;j<BazarPrice.length;j++){
                System.out.println(BazarPrice[i][j]);
            }

        }


    }
}
